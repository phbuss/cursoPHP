<?php

  //Operadores aritméticos
  $a = 10;
  $b = 3;

  echo "A: " . $a . "<br>";
  echo "B: " . $b . "<br>";

  echo "A + B: " . ($a + $b) . "<br>";
  echo "A - B: " . ($a - $b) . "<br>";
  echo "A * B: " . ($a * $b) . "<br>";
  echo "A / B: " . ($a / $b) . "<br>";
  echo "A % B: " . ($a % $b) . "<br>"; //módulo (resto da divisão)
  echo "A ** B: " . ($a ** $b) . "<br>"; //exponenciação

?>
